    <h4>English</h4>

    <p>
      GambiConf is a friendly, inclusive and harassment-free environment where everyone is welcome and where civility is compulsory.
    </p>

    <p>
      Conference organizers act to guarantee such an environment.
    </p>

    <p>
      Therefore:
    </p>

    <ol>
      <li>
        We will not tolerate harassment, inappropriate discrimination or public humiliation of any kind;
      </li>
      <li>
        Regardless of their role every person taking part in the conference is subject to these rules;
      </li>
    </ol>

    <p>
      Thus we understand:
    </p>

    <ul>
      <li>
        Harassment is the act of insisting, persecuting or coercing someone into involuntary behavior.
      </li>
      <li>
        Inappropriate discrimination is the act of pulling apart, insulting or humiliating someone by promoting their exclusion
        by any particular attribute.
      </li>
      <li>
        Public humiliation is the act of subjugating, pulling down, ridiculing or publicly promoting the vexation of others.
      </li>
    </ul>

    <p>
      If you feel harassed, discriminated or humiliated, or if you witnessed any of these behaviors, please contact the organizers.
    </p>

    <p>
      In face of any allegation of violation of these principles, the organizers will examine and consider the case in order to
      take action to ensure that such events will not happen again. These actions might take the form of a conversation to reconcile
      the parts, an invitation to leave the event indefinitely, or any other form to keep up with the values stated in this Code of Conduct.
    </p>
  </article>

